let thu = +prompt("Hãy nhập vào 1 số");
switch (thu) {
    case 2:
        console.log("monday")
        break;
    case 3:
        console.log("tuesday")
        break;
    case 4:
        console.log("wednesday")
        break;
    case 5:
        console.log("thursday")
        break;
    case 6:
        console.log("friday")
        break;
    case 7:
        console.log("saturday")
        break;
    case 8:
        console.log("sunday")
        break;
    default:
        console.log("không hợp lệ")
}
